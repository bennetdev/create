import enum


class TokenType(enum.Enum):
    EOF = "\0"
    NEWLINE = "\n"
    NUMBER = "NUMBER"
    IDENT = "IDENT"
    STRING = "STRING"
    # Keywords
    VAR_ASSIGN = "var"
    IF = "if"
    ELSE = "else"
    ELSEIF = "elseif"
    THEN = "{"
    END = "}"
    WHILE = "while"
    REPEAT = "repeat"
    EACH = "each"
    IN = "in"
    # Operators.
    EQ = "="
    PLUS = "+"
    PLUSEQ = "+="
    ASTERISKEQ = "*="
    MINUSEQ = "-="
    SLASHEQ = "/="
    MINUS = "-"
    ASTERISK = "*"
    SLASH = "/"
    PLUSPLUS = "++"
    MINUSMINUS = "--"
    EQEQ = "=="
    NOTEQ = "!="
    LT = "<"
    LTEQ = "<="
    GT = ">"
    GTEQ = ">="
    # Misc
    OPAREN = "("
    CPAREN = ")"
    SEPERATOR = ","
    OBRACKET = "["
    CBRACKET = "]"
    COMMENT = "#"
